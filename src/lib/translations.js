/** Simple placeholder replace for {key} in translation strings */
export function interpolate(str, vars = {}) {
  if (!str) return str
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v)),
    str
  )
}
